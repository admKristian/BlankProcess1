    public class Solution : CodedWorkflow
    {
        [Workflow]
        public bool isPalindrome(string s)
        {
            int left = 0;
            int right = s.Length - 1;

            while (left < right)
            {
                if (s[left] != s[right])
                {
                    return false;
                }

                left++;
                right--;
            }
            return true;
        }

